function __addNameTagToModel__(tag, model) {
    var kvs = tag.split(/\s*,\s*/);

    if (!(model instanceof AMI)) {
        model.name = '';
    }

    model.comment = '';

    for (var i = 0; i < kvs.length; i++) {
        var kv = kvs[i].split(/\s*:\s*/, 2);
        var key = kv[0].trim();
        var value = (kv[1] || "").trim();

        if (key == "Name") {
            model.name = value;
        }

        if (key == "Comment") {
            model.comment = value;
        }
    }
}

